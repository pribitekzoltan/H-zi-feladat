    <h1>Zöld Remény Alapítvány - Green Hope</h1>
    <div class="tartalom">
      <p>
        „Úgy gondolom, a Természet fantáziája annyival nagyszerűbb az emberénél, hogy sosem hagy majd nyugodni minket.”
      </p>
      <p>
        Richard Feynman – amerikai fizikus
      </p>
      <p>
        Régóta terveztük, hogy létrehozunk egy alapítványt, mellyel segíthetünk másokon, környezetünkön, a rászorulókon.
      </p>
      <p>
        Szükségét éreztük annak, hogy egy olyan alapítványt hozzunk létre, amiben a támogatóink és szimpatizánsaink részt vehetnek, hogy együtt tegyünk a környezetünkért, segítsük rászoruló embertársainkat.
      </p>
      <p>
        Az ötletet tett követte, létrehoztuk az alapítványt, mely három fő területen tevékenykedik: környezetvédelem, állatok védelme, illetve szociálisan rászoruló embertársaink támogatása.
      </p>
      <p>
        Alapítványunkat Green Hope-nak, vagyis Zöld Reménynek neveztük el; ez a név magában hordozza filozófiánkat, vagyis, hogy szeretnénk segíteni környezetünkön. Hisszük és reméljük, hogy nem késő. Valljuk, hogy összefogással és némi segítséggel szebbé, élhetőbbé tudjuk tenni mindennapjainkat.
      </p>
      <p>
        Más szervezetekkel, egyesületekkel együttműködve igyekszünk óvni, tisztábbá, egészségesebbé tenni környezetünket.
      </p>
      <p>
        Ehhez keressük társainkat, támogatóinkat, akik velünk tartanak a környezet megóvásában, és reméljük, hogy ezáltal egyre több lesz azon emberek száma, akik meg akarják óvni Földünket a pusztulástól.
      </p>
      <p>
        Másik fő területünk az állatok védelme: vadon élő állatok természetes élőhelyének megóvása, kihalófélben lévő fajok megmentése, de például kóbor kutyák menhelyre történő eljuttatása ugyanúgy szívügyünk.
      </p>
      <p>
        Harmadik fő irányunk az emberek támogatása, akik szociális problémákkal küzdenek; egyedül élő idős emberek segítése, nagycsaládosok, nehéz anyagi helyzetben élők támogatása.
      </p>
      <p>
        Nem csodákat kívánunk tenni, de valljuk, hogy minden apró tettel előrébb vagyunk. A célt tudjuk, és nem habozunk elindulni ezen a nehéz és rögös úton.
      </p> 
      <p>
        Green Hope!
      </p>
      
